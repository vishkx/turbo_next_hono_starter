import * as React from "react";
import Link from "next/link";

import { siteConfig } from "@/lib/config";
import { Icons } from "./icons";
import { Button } from "@/components/ui/button";
import { Skeleton } from "@/components/ui/skeleton";

export function GitHubLink() {
  return (
    <Button asChild size="sm" variant="ghost" className="h-8 shadow-none">
      <Link href={siteConfig.links.github} target="_blank" rel="noreferrer">
        <Icons.gitHub />
        <React.Suspense fallback={<Skeleton className="h-4 w-[42px]" />}>
          <StarsCount />
        </React.Suspense>
      </Link>
    </Button>
  );
}

export async function StarsCount() {
  const data = await fetch(
    siteConfig.links.github_api || "",
    {
      next: { revalidate: 60 * 60 }, // 1 hour
    },
  );
  const json = await data.json();

  // Handle API errors (rate limiting, not found, etc.)
  if (!data.ok || typeof json.stargazers_count !== "number") {
    return (
      <span className="text-muted-foreground w-fit text-xs tabular-nums">
        0
      </span>
    );
  }

  const formattedCount =
    json.stargazers_count >= 1000
      ? `${Math.round(json.stargazers_count / 1000)}k`
      : json.stargazers_count.toLocaleString();

  return (
    <span className="text-muted-foreground w-fit text-xs tabular-nums">
      {formattedCount}
    </span>
  );
}
